# --- 1. DATABASE LOGIC (History & Storage) ---
def init_db():
    conn = sqlite3.connect('meetings.db')
    c = conn.cursor()
    c.execute('''CREATE TABLE IF NOT EXISTS meeting_history 
                 (id INTEGER PRIMARY KEY, date TEXT, title TEXT, summary TEXT, actions TEXT, transcript TEXT)''')
    conn.commit()
    conn.close()

def create_pdf(title, summary, actions, date):
    pdf_path = f"Meeting_Minutes_{date.replace(':', '-')}.pdf"
    c = canvas.Canvas(pdf_path, pagesize=letter)
    
    # PDF Header Design
    c.setFont("Helvetica-Bold", 18)
    c.drawString(100, 750, "OFFICIAL MEETING MINUTES")
    c.setFont("Helvetica", 10)
    c.drawString(100, 735, f"Date: {date} | Generated by AI Intelligence")
    c.line(100, 725, 500, 725)
    
    # Meeting Body
    c.setFont("Helvetica-Bold", 12)
    c.drawString(100, 700, f"SUBJECT: {title.upper()}")
    
    c.setFont("Helvetica-Bold", 12)
    c.drawString(100, 670, "EXECUTIVE SUMMARY:")
    c.setFont("Helvetica", 10)
    y_pos = 650
    # Text wrapping for long summaries
    for line in [summary[i:i+85] for i in range(0, len(summary), 85)]:
        c.drawString(100, y_pos, line)
        y_pos -= 15
    
    c.setFont("Helvetica-Bold", 12)
    c.drawString(100, y_pos - 20, "KEY DELIVERABLES & DEADLINES:")
    y_pos -= 40
    for item in actions.split('\n'):
        if item.strip():
            c.drawString(110, y_pos, f"• {item.strip()}")
            y_pos -= 15
            
    c.save()
    return pdf_path

init_db()

